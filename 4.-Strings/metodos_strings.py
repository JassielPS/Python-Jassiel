# Ejemplo 1: upper() y lower()
print("Hola".upper())  # HOLA
print("HOLA".lower())  # hola

# Ejemplo 2: split()
palabras = "Python es genial".split()
print(palabras)  # ['Python', 'es', 'genial']

# Ejemplo 3: replace()
nuevo_texto = "Java es mejor".replace("Java", "Python")
print(nuevo_texto)  # Python es mejor
